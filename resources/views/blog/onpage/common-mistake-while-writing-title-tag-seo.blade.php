                <p><b>2. Duplicate title:</b> Duplicate title means if more than one webpage of a website have same title. It may decrease your ctr and bad user experience for your audience. So always try to write unique title for every webpage.</p>
              </div>
              <div class="single-blogs-steps">
                <p><b>3. keyword stuffing:</b> Don't just stuff keywords into your title tag, Sometimes people use keyword randomly in the title tag to increase ctr. But it is not a good practice in seo, Always use keywords in such a way so that it looks natural when user read it.</p>
