                <p><b>4. Multiple target keywords:</b> Sometimes people use so many keywords in title tag to cover all the topics present in the webpage. But it will not increase your ranking and user will not click to your link. So if you have more topics try to seperate them in different webpages and after that you can write title for individual webpages.</p>
              </div>
              <div class="single-blogs-steps">
                <p><b>5. Unrelevancy:</b> Your title tag should be the perfect idea of content present in your webpage. But sometimes people mistakely write totally irrelevant title for their webpage. In such a condition user cannot get the idea of your webpage and may skip your link in the serp.</p>
              </div>
              <div class="single-blogs-steps">
                <p><b>6. Brand Name Usage:</b> Google suggest to write your title in 60-70 characters. But when we use our brand or company name in title tag it can take 30-40% space and restrict us to write more detailed title. So don't use your company name until its necessary. This will allow you to write more detailed title and help your audience for better understanding of your webpage.</p>
              </div>
              <div class="single-blogs-steps">
                <p><b>7. Long title:</b> As above I told you Google suggest the length of title tag should be between 60-70 characters. If you write long title more than 60-70 characters , Google will cutoff the extra portion. It may possible that some important words will remove. So always keep it in mind and write the title tag between 60-70 characters for better practice in seo.</p>
              </div>
